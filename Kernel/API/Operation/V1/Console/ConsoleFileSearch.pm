package Kernel::API::Operation::V1::Console::ConsoleFileSearch;

use strict;
use warnings;

use Kernel::System::VariableCheck qw(:all);

use base qw(
    Kernel::API::Operation::V1::Common
);

our $ObjectManagerDisabled = 1;

=head1 NAME

Kernel::API::Operation::Console::ConsoleFileSearch - API ConsoleFile Search Operation backend

=head1 PUBLIC INTERFACE

=over 4

=cut

=item Run()

perform ConsoleFileSearch Operation. This will return a ConsoleFile list.

    my $Result = $OperationObject->Run(
        Data => {
        }
    );

    $Result = {
        Success => 1,                                # 0 or 1
        Code    => '',                          # In case of an error
        Message => '',                          # In case of an error
        Data    => {
            ConsoleFile => [
                {},
                {}
            ]
        },
    };

=cut

sub Run {
    my ( $Self, %Param ) = @_;

    # get file list
    my @FileList = $Kernel::OM->Get('Console')->FileList();

    # return result
    return $Self->_Success(
        ConsoleFile => \@FileList,
    );
}

1;
=back


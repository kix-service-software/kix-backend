package Kernel::System::Automation::Event::Handler;

use strict;
use warnings;

our @ObjectDependencies = (
    'Config',
    'Log',
    'Automation',
);

sub new {
    my ( $Type, %Param ) = @_;

    # allocate new hash for object
    my $Self = {};
    bless( $Self, $Type );

    return $Self;
}

sub Run {
    my ( $Self, %Param ) = @_;

    # check needed stuff
    for my $Needed (qw(Event Data Config UserID)) {
        if ( !$Param{$Needed} ) {
            $Kernel::OM->Get('Log')->Log(
                Priority => 'error',
                Message  => "Need $Needed!",
            );
            return;
        }
    }

    return $Kernel::OM->Get('Automation')->ExecuteJobsForEvent(
        %Param,
        Type   => $Param{Config}->{Type},
        UserID => 1,        # always execute with privileged user
    );
}

1;



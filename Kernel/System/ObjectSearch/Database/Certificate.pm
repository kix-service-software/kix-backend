sub Init {
    my ( $Self, %Param ) = @_;

    # init join map as empty hash
    $Param{Flags}->{JoinMap} = {};

    # init certificate join counter with 0
    $Param{Flags}->{CertificateJoinCounter} = 0;

    return 1;
}

sub GetBaseDef {
    my ( $Self, %Param ) = @_;

    return {
        Select  => ['vfs.id', 'vfs.filename'],
        From    => ['virtual_fs vfs'],
        OrderBy => ['vfs.id ASC']
    };
}

1;


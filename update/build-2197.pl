use strict;
use warnings;

use File::Basename;
use FindBin qw($Bin);
use lib dirname($Bin);
use lib dirname($Bin) . '/Kernel/cpan-lib';

use Kernel::System::ObjectManager;
use Kernel::System::VariableCheck qw(:all);

# create object manager
local $Kernel::OM = Kernel::System::ObjectManager->new(
    'Log' => {
        LogPrefix => 'framework_update-to-build-2197',
    },
);

use vars qw(%INC);

_UpdateCounters();

sub _UpdateCounters {
    my ( $Self, %Param ) = @_;

    $Kernel::OM->Get('User')->UpdateCounters(
        UserID => 1,
    );
    $Kernel::OM->Get('Cache')->CleanUp();

    return 1;
}

exit 0;


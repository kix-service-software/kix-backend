use strict;
use warnings;

use File::Basename;
use FindBin qw($Bin);
use lib dirname($Bin);
use lib dirname($Bin) . '/Kernel/cpan-lib';

use Kernel::System::ObjectManager;
use Kernel::System::VariableCheck qw(:all);
use Kernel::System::Role::Permission;

# create object manager
local $Kernel::OM = Kernel::System::ObjectManager->new(
    'Log' => {
        LogPrefix => 'framework_update-to-build-1528',
    },
);

use vars qw(%INC);

_RemoveAbandonedJobLogs();

exit 0;

sub _RemoveAbandonedJobLogs {
    my ( $Self, %Param ) = @_;

    return if !$Kernel::OM->Get('DB')->Do(
        SQL  => 'DELETE FROM automation_log WHERE job_id NOT IN (select job.id FROM job)'
    );

    # delete whole cache
    $Kernel::OM->Get('Cache')->CleanUp();

    return 1;
}

exit 0;

